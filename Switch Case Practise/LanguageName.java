package switchcase;

public class LanguageName {

	public static void main(String[] args) {
		int choice = 1;
		switch (choice) {
		case 1:
			System.out.println("JAVA");
			break;
		case 2:
			System.out.println("PYTHON");
			break;
		case 3:
			System.out.println("C++");
			break;
		case 4:
			System.out.println("JAVASCRIPT");
			break;
		default:
			System.out.println("Invaild Inputs .....!");

		}

	}

}
