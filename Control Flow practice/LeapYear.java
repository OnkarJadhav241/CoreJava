package ifelsepracticeset;

public class LeapYear {

	public static void main(String[] args) {
		
		int year =2005;
		
		if(year%4==0) {
			System.out.println("The year "+year+" is a Leap year");
		}else {
			System.out.println("The year "+year+" is not a Leap year");
		}
			
	}

}
